/** 
 * Program berikut untuk mencoba seleksi if-else
*/
public class IfElseExample {
	public static void main(String[] args) {
		int number=13;
		if(number%2==0) { 
			System.out.println("Angka genap");
		}else{
			System.out.println("Angka ganjil");
		}
	}
}

/*
program code untuk mengecek apakah 13 bilangan ganjil atau genap. angka 13 tidak menghasilkan sisa 0
jika dibagi dua, maka akan dicetak "Angka ganjil"
*/